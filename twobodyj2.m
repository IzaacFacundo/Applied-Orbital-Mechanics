function dY = twobodyj2(t,y)
    mu = 398600.4415000;
    J2 = 0.001082480000;
    dY = zeros(6,1);
    dY(1:3) = y(4:6);
    re = 6378.136300000;
    r = norm(y(1:3));
    format long
    dY(4) = ((-1*mu*y(1))/(r^3))*(1 - J2*(3/2)*((re/r)^2)*(5*(y(3)/r)^2 - 1));
    dY(5) = ((-1*mu*y(2))/(r^3))*(1 - J2*(3/2)*((re/r)^2)*(5*(y(3)/r)^2 - 1));
    dY(6) = ((-1*mu*y(3))/(r^3))*(1 - J2*(3/2)*((re/r)^2)*(5*(y(3)/r)^2 - 3));
end